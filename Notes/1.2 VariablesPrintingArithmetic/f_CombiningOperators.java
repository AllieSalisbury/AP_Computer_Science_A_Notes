public class f_CombiningOperators
    {
    public static void main(String[] args)
        {
        /*
        Java follows the order of operations. We treat % as division in that order. 

        What should get printed? 
        */
        
        System.out.println("--------------------");

        int a = 5 + (4 * 2);
        System.out.println(a);
        
        double b = 4.5 * 2 - 3.7;
        System.out.println(b);

        double c = 7.5 / 3 + 4; 
        System.out.println(c); 

    }
}